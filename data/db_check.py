import sqlite3
db = sqlite3.connect(r'C:\Users\simon\.openclaw\workspace\ecolibrium\data\ecolibrium_directory.db')
c = db.cursor()
c.execute('PRAGMA table_info(organizations)')
print('SCHEMA:')
for r in c.fetchall(): print(' ', r)
c.execute("SELECT status, COUNT(*) as n FROM organizations GROUP BY status")
print('STATUS:')
for r in c.fetchall(): print(' ', r)
c.execute("SELECT city, state_province, COUNT(*) as n FROM organizations WHERE country_code='US' GROUP BY city, state_province ORDER BY n DESC LIMIT 5")
print('TOP US CITIES:')
for r in c.fetchall(): print(' ', r)
c.execute("SELECT name, city, state_province, annual_revenue FROM organizations WHERE country_code='US' LIMIT 3")
print('SAMPLE US:')
for r in c.fetchall(): print(' ', dict(zip(['name','city','state','rev'], r)))
db.close()
